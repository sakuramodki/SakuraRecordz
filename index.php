<?php
    $csvPath = "https://docs.google.com/spreadsheets/d/e/2PACX-1vTWVH6vFIptjOhpNY5i9bsWHpYgCf2HjTjYeLKcLbyZexk83J2i1Ijte-SBPOXFF17lB6vglgzRKvI4/pub?output=csv";
